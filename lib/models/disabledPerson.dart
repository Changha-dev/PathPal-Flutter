class DisabledPerson {
  final String? uid;
  final String? profileUrl;
  final String? email;
  final String? name;
  final String? phoneNumber;
  final String? disabilityType;
  final String? wcUse;

  DisabledPerson(
      {required this.uid,
      required this.profileUrl,
      required this.email,
      required this.name,
      required this.phoneNumber,
      required this.disabilityType,
      required this.wcUse});
}
